const getLimitDate = (data) => {
  let limitDate = "";

  try {
    if (data.buyDate) {
      const d = new Date(`${data.buyDate}T10:00:00`);
      d.setDate(d.getDate() + 3);
      const dia = String(d.getDate()).padStart(2, "0");
      const mes = String(d.getMonth() + 1).padStart(2, "0");
      const ano = d.getFullYear();
      limitDate = `${dia}/${mes}/${ano}`;
    }
  } catch (error) {}

  return limitDate;
};

